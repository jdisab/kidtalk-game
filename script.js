let selectedCategories = new Set();
let usedCards = [];
let currentCard = null;
let ageMode = "all";
let favorites = JSON.parse(localStorage.getItem("kidtalkFavorites") || "[]");

const categoryButtons = document.getElementById("categoryButtons");
const cardCategory = document.getElementById("cardCategory");
const cardQuestion = document.getElementById("cardQuestion");
const cardPrompt = document.getElementById("cardPrompt");
const drawBtn = document.getElementById("drawBtn");
const resetBtn = document.getElementById("resetBtn");
const easyBtn = document.getElementById("easyBtn");
const deeperBtn = document.getElementById("deeperBtn");
const originalBtn = document.getElementById("originalBtn");
const favoriteBtn = document.getElementById("favoriteBtn");
const cardEl = document.getElementById("card");
const favoritesList = document.getElementById("favoritesList");
const countBadge = document.getElementById("countBadge");

function getCategories() {
  return [...new Set(cards.map(card => card.category))];
}

function buildCategoryButtons() {
  getCategories().forEach(category => {
    const info = categoryInfo[category] || { emoji: "🃏", color: "#eeeeee" };
    const btn = document.createElement("button");
    btn.className = "category-btn";
    btn.textContent = `${info.emoji} ${category}`;
    btn.style.background = info.color;
    btn.addEventListener("click", () => {
      if (selectedCategories.has(category)) {
        selectedCategories.delete(category);
        btn.classList.remove("active");
      } else {
        selectedCategories.add(category);
        btn.classList.add("active");
      }
      updateCount();
    });
    categoryButtons.appendChild(btn);
  });
}

function eligibleCards() {
  return cards.filter(card => {
    const ageMatch = ageMode === "all" || card.ages.includes(ageMode);
    const catMatch = selectedCategories.size === 0 || selectedCategories.has(card.category);
    return ageMatch && catMatch;
  });
}

function updateCount() {
  countBadge.textContent = `${eligibleCards().length} cards`;
}

function drawCard() {
  let availableCards = eligibleCards().filter(card => !usedCards.includes(card));
  if (availableCards.length === 0) {
    usedCards = [];
    availableCards = eligibleCards();
  }
  if (availableCards.length === 0) {
    cardQuestion.textContent = "No cards match those filters.";
    cardPrompt.textContent = "Try selecting fewer categories or changing the age mode.";
    return;
  }
  currentCard = availableCards[Math.floor(Math.random() * availableCards.length)];
  usedCards.push(currentCard);
  showCard(currentCard.question, "Kids can answer with words, pointing, drawing, animals, colors, movement, or “pass.”");
}

function showCard(questionText, promptText) {
  if (!currentCard) return;
  const info = categoryInfo[currentCard.category] || { emoji: "🃏", color: "#5e35b1" };
  cardCategory.textContent = `${info.emoji} ${currentCard.category}`;
  cardQuestion.textContent = questionText;
  cardPrompt.textContent = promptText;
  cardEl.style.borderColor = info.color;
}

function renderFavorites() {
  if (favorites.length === 0) {
    favoritesList.textContent = "No favorites saved yet.";
    return;
  }
  favoritesList.innerHTML = "";
  favorites.forEach((fav, index) => {
    const div = document.createElement("div");
    div.className = "favorite-item";
    div.innerHTML = `<strong>${fav.category}</strong><br>${fav.question}`;
    div.addEventListener("click", () => {
      currentCard = fav;
      showCard(fav.question, "Favorite card loaded.");
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
    favoritesList.appendChild(div);
  });
}

