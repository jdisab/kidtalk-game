favoriteBtn.addEventListener("click", () => {
  if (!currentCard) return;
  const exists = favorites.some(f => f.question === currentCard.question);
  if (!exists) {
    favorites.push(currentCard);
    localStorage.setItem("kidtalkFavorites", JSON.stringify(favorites));
    renderFavorites();
  }
});

